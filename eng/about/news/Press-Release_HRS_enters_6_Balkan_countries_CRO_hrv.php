    <p><strong>Skupina tvrtki „HRS” počinje raditi u šest zemalja Balkanskog poluotoka</strong></p>
     
<p>27/12/2016, Zagreb. Tvrtka „HRS (Hospitality and Retail Systems)“ оbjavila je novost o proširenju i početku poslovanja u Hrvatskoj, Sloveniji, Bosni i Hercegovini, Маkedoniji, na Kosovu i u Albaniji.</p>

<p>Više od 26 godina „HRS” uspješno stvara najinovativnija i najučinkovitija IT rješenja u sektorima hotelijerstva, ugostiteljstva i maloprodaje. Danas je tvrtka zastupljena u 26 zemalja u 12 vremenskih zona i poslužuje preko 2700 klijenata.</p>

<p>„Balkan je poznat po svojoj tradicionalnoj gostoljubivosti i mi želimo podržati daljnji regionalni razvoj u sektorima hotelijerstva, ugostiteljstva i maloprodaje. Uvjereni smo da će naši novi klijenti cijeniti najinovativnija IT rješenja i profesionalizam naših tehničkih stručnjaka,“ – izjavila je Joanne Vaughan, utemeljiteljica i predsjednica uprave skupine tvrtki „HRS“.</p>

<p>Za partnere naše tvrtke iz drugih zemalja, izlazak na tržište balkanskih zemalja označava jačanje tehničkoga tima i daljnji razvoj stručnosti tvrtke u implementaciji i pružanju podrške specijaliziranim rješenjima za sektore hotelijerstva, ugostiteljstva i maloprodaje.</p>

<p>&nbsp;</p>

<p>O tvrtki „HRS“</p>
<p>«Hospitality & Retail Systems (HRS)» - je međunarodna tvrtka, lider tržišta u segmentu IT rješenja u sektoru hotelijerstva, ugostiteljstva i maloprodaje u Središnjoj i Istočnoj Europi i Središnjoj Aziji. Više od 26 godina „HRS” pruža inovativna rješenja za hotele, restorane, maloprodajne lance, stadione, SPA i fitnes klubove. Između ostalog, „HRS“ pruža usluge pretprojektnih analiza, isporuke hardvera i softvera, strateškog konzaltinga u poslovanju, usluge implementacije i integracije različitih rješenja, edukacije krajnjih korisnika, danonoćne tehničke podrške, te mnoštvo drugih usluga za klijente. </p>

