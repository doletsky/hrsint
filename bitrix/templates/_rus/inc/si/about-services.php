<ul class="check-list"> 						 
  <li><i></i>Imate majhno restavracijo, ki ne zahteva zapletene in drage
      avtomatizacije?</li>
 	
  <li><i></i>Bi morali samo videti svoje prihodke, nadzorovati ostanke v skladiščih, razumeti stroške dela in donosnost podjetja? </li>
 
  <li><i></i>Potrebujete sistem, ki je zaščiten pred vdori in zlorabo?</li>
 						 	
  <li><i></i>Ne želite biti odvisni od kompliciranega kontrolnega sistema?</li>
 	 
  <li><i></i>Ne želite plačevati za funkcije, ki jih ne potrebujete?</li>
 						 
  <li><i></i>Ne želite neprestano menjati opremo, ker jo prenehajo podpirati novi operacijski sistemi in nove različice programske opreme?</li>
 					</ul>
 					 
